export class Person {
    id: string;
    name: string;
    age: number;
    hello: string;
    desc: string;
    constructor(id: string, name: string, age: number, hello: string, desc: string) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.hello = hello;
        this.desc = desc;
    }

}